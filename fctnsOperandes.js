function sum(a,b) {
    a = parseFloat(a);
    b = parseFloat(b);
    return (a+b);
}

function minus(a,b) {
    a = parseFloat(a);
    b = parseFloat(b);
    return (a-b);
}

function multiply(a,b) {
    a = parseFloat(a);
    b = parseFloat(b);
    return (a*b);
}

function divide(a,b) {
    a = parseFloat(a);
    b = parseFloat(b);
    return (a/b);
}